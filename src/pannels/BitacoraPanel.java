    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblBitacora.getModel();
        model.setRowCount(0);
        
        ResultSet rs = controllers.BitacoraController.getBitacora();
        
        try {
            while(rs.next()){
               model.addRow(new Object[]{rs.getString("userID"), rs.getString("action"), rs.getString("createdAt")});
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }

