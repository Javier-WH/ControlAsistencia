    private ResultSet loadData(){
       return controllers.BitacoraController.getBitacora();
    }
    
    private void loadTable(){
        try {
            ResultSet data = loadData();
            
            if(data == null){
            JOptionPane.showMessageDialog(this, "No hay datos que mostar");
            }else{
            
                while (data.next()) {
                    String action = data.getString("action");
                    String date = data.getString("createdAt");
                    tableModel.addRow(new Object[]{action, date });    
                }
            }


        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            System.out.print(e);
        }
        
        
    
    }

